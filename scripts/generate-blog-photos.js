const fs = require('fs');
const path = require('path');

const rootDir = path.resolve(__dirname, '..');
const blogImagesDir = path.join(rootDir, 'public', 'images', 'blog');
const outputFile = path.join(rootDir, 'src', 'data', 'blogPhotos.generated.js');

const imageExtensions = new Set(['.jpg', '.jpeg', '.png', '.webp', '.gif']);

const naturalCompare = (a, b) => a.localeCompare(b, undefined, {
  numeric: true,
  sensitivity: 'base',
});

const toPublicPath = (folder, file) => `/images/blog/${folder}/${file}`;

const quote = (value) => `'${value.replace(/\\/g, '\\\\').replace(/'/g, "\\'")}'`;

const photosByFolder = fs.readdirSync(blogImagesDir, { withFileTypes: true })
  .filter((entry) => entry.isDirectory() && !entry.name.startsWith('.'))
  .sort((a, b) => naturalCompare(a.name, b.name))
  .reduce((acc, entry) => {
    const files = fs.readdirSync(path.join(blogImagesDir, entry.name), { withFileTypes: true })
      .filter((file) => file.isFile())
      .map((file) => file.name)
      .filter((file) => !file.startsWith('.'))
      .filter((file) => file.toLowerCase() !== 'cover.jpg')
      .filter((file) => imageExtensions.has(path.extname(file).toLowerCase()))
      .sort(naturalCompare)
      .map((file) => toPublicPath(entry.name, file));

    acc[entry.name] = files;
    return acc;
  }, {});

const entries = Object.entries(photosByFolder)
  .map(([folder, photos]) => {
    const values = photos.map((photo) => `    ${quote(photo)},`).join('\n');
    return photos.length > 0
      ? `  ${folder}: [\n${values}\n  ],`
      : `  ${folder}: [],`;
  })
  .join('\n');

const output = `// This file is generated by scripts/generate-blog-photos.js.
// Do not edit manually.

const blogPhotos = {
${entries}
};

export default blogPhotos;
`;

fs.writeFileSync(outputFile, output);
